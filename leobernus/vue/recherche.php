			<h1>Recherche scientifique</h1>
			<p>J'ai travaillé à l'Observatoire de Paris sous la direction de <a href="https://perso.imcce.fr/jacques-laskar/fr/">Jacques Laskar</a> (Institut de Mécanique Céleste et Calcul des Éphémérides) et <a href="https://www.iau.org/administration/membership/individual/11351/">Agnès Fienga</a> (Géoazur) sur ma thèse dont le sujet est intitulé : <em>Tests de gravitation à l'échelle du système solaire.</em></p>
			<p>Depuis environ cinq ans, je m'intéresse à la phénoménologie des théories alternatives à la relativité générale d'Einstein. Lorsque cette phénoménologie présente des différences potentiellement mesurables avec celle de la relativité générale à l'échelle du système solaire, j'utilise l'éphéméride planétaire <a href="https://www.imcce.fr/inpop">INPOP</a> (Intégration Numérique Planétaire de l'Observatoire de Paris) pour vérifier que cette phénoménologie est compatible avec les observations les plus précises dont on dispose. Ces tests sont donc en permanente évolution, car chaque année nous disposons de nouvelles données planétaires.</p>
			<p>Mon travail de phénoménologue se situe donc à l'interface entre la théorie et l'expérimentation. Je dois comprendre les théories alternatives pour en extraire leur phénoménologie, et je dois comprendre les données planétaires pour les exploiter et les comparer à une modélisation numérique des théories testées.</p>

